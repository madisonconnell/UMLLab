
public class Order extends Account{
	int price;
	String productName;
	String dateOrder;
	int orderNum;
	
	public int getPrice()
	{
		return price;
	}
	public String getProductName()
	{
		return productName;
	}
	public String getDateOrder()
	{
		return dateOrder;
	}
	public int getOrderNum()
	{
		return orderNum;
	}
	
}
